package com.trinh.japanese.model;

public class Example {
    private String s;
    private String d;
    private String id;

    public Example(String s, String d, String id) {
        this.s = s;
        this.d = d;
        this.id = id;
    }

    public String getS() {
        return s;
    }

    public void setS(String s) {
        this.s = s;
    }

    public String getD() {
        return d;
    }

    public void setD(String d) {
        this.d = d;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }
}
